#include <iostream>
#include <string>
using std::cin; using std::cout;
using std::endl; using std::string;

int main(){
    // Line at a time
    string s;

    // Delete one to run
    // Reading inputs by line until EOF
    while (getline(cin, s))
        return 0;

    // Reading inputs by word until EOF
    while (cin >> s)
        return 1;
}
